    private void registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarActionPerformed
        // registrar la materia del estudiante con su codigo
        if (contador2 < matr.getCantMaterias()) {
            try {
                String codigoEst = comboCodigos.getSelectedItem().toString();
                String semester = semestre.getSelectedItem().toString();
                String matery = materia.getText();

                materias mat = new materias();

                mat.setCodigoEstudiante(codigoEst);
                mat.setSemestre(semester);
                mat.setMateria(matery);

                agregarMateriaComboBox(matery, ComboMateria);
                agregarMateriaComboBox(matery, materiaConsulta);

                matr.setMaterias(mat, contador2);

                contador2++;
                limpiarNotas();

            } catch (Exception e) {
                mensajeError("Complete los campos para continuar" + e);
            }

        } else {
            mensajeError("Cantidad de Materias completadas");
        }
    }//GEN-LAST:event_registrarActionPerformed

    private void RegNotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegNotaActionPerformed
        try {
            String materia = ComboMateria.getSelectedItem().toString();
            String codigoEst = codEstN.getSelectedItem().toString();

            int PosMat = matr.obtenerPosicionMateria(codigoEst, materia);

            float nota1 = Float.parseFloat(notaUno.getText());
            float nota2 = Float.parseFloat(notaDos.getText());
            float nota3 = Float.parseFloat(notaTres.getText());

            materias materiaEncontrada = matr.getArregloMaterias(PosMat);

            materiaEncontrada.setNota1(nota1);
            materiaEncontrada.setNota2(nota2);
            materiaEncontrada.setNota3(nota3);

            limpiarCalificaciones();

        } catch (Exception e) {
            mensajeError("Completa los campos para guardar la nota!");
        }

    }//GEN-LAST:event_RegNotaActionPerformed

    private void buscarMateriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarMateriaActionPerformed
        //Buscamos por Materia el codigo del estudiante para asignarle las tres notas
        codEstN.removeAllItems();
        try {
            for (int i = 0; i < matr.getCantMaterias(); i++) {
                if (matr.getArregloMaterias(i) == null) {
                    continue;
                }
                if ((ComboMateria.getSelectedItem().toString()).equals(matr.getArregloMaterias(i).getMateria())) {
                    codEstN.addItem(matr.getArregloMaterias(i).getCodigoEstudiante());
                    codConsulta.addItem(matr.getArregloEstudiantes(i).getCodigoEstudiante());
                }
            }
        } catch (Exception e) {
            System.out.println(e);
        }

    }//GEN-LAST:event_buscarMateriaActionPerformed

