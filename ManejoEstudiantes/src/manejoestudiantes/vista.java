    private void matricularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_matricularActionPerformed
        // matricular el estudiante, guardar en los arreglos
        if (contadorEst < matr.getCantEstudiantes()) {
            try {
                estudiantes est = new estudiantes();

                String codigo = cod.getText();
                est.setCodigoEstudiante(codigo);

                String nombre = name.getText();
                est.setNombre(nombre);

                Date fdn = fechaNacimiento.getDate();
                est.setFechaDeNacimiento(fdn);

                String sex;
                if (masculino.isSelected()) {
                    sex = "Masculino";
                } else {
                    sex = "Femenino";
                }
                est.setSexo(sex);

                String carrera = carr.getText();
                est.setSexo(sex);

                String telefono = tel.getText();
                est.setTelefono(telefono);

                String correo = email.getText();
                est.setCorreo(correo);

                matr.setEstudiantes(est, contadorEst);

                comboCodigos.addItem(codigo);

                contadorEst++;

                limpiarRegistroEst();

            } catch (Exception e) {
                mensajeError("Complete los campos para continuar");
            }
        } else {
            mensajeError("El arreglo se encuentra lleno");
        }
    }//GEN-LAST:event_matricularActionPerformed

