    private void promedioGeneralActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_promedioGeneralActionPerformed
        //Consulta el promedio general
        areaTres.setText("");
        float nota = 0;
        int numEst = 0;
        float promedio = 0;
        try {

            for (int i = 0; i < matr.getCantMaterias(); i++) {
                if (matr.getArregloMaterias(i) == null) {
                    continue;
                }

                if ((materiaConsulta.getSelectedItem().toString()).equals(matr.getArregloMaterias(i).getMateria())) {
                    nota += matr.getArregloMaterias(i).calcNotaFinal();
                    numEst++;

                }
            }

        } catch (Exception e) {
            System.out.println(e);
        }
        promedio = nota / numEst;
        areaTres.append("Materia: " + materiaConsulta.getSelectedItem().toString() + "\n"
                + "Promedio General: " + promedio + "\n");


    }//GEN-LAST:event_promedioGeneralActionPerformed

