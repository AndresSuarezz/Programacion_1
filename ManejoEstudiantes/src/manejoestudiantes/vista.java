    private void mejorNotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mejorNotaActionPerformed
        //Consultar mejor nota
        float nota = 0;
        int buscando = 0;
        String name = "";
        String matery = "";
        try {
            for (int i = 0; i < matr.getCantMaterias(); i++) {

                if (matr.getArregloMaterias(i) == null) {
                    continue;
                }

                if (nota < matr.getArregloMaterias(i).calcNotaFinal()) {

                    matery = matr.getArregloMaterias(i).getMateria();
                    buscando = matr.buscarEstudiante(matr.getArregloMaterias(i).getCodigoEstudiante());
                    name = matr.getArregloEstudiantes(buscando).getNombre();
                    nota = matr.getArregloMaterias(i).calcNotaFinal();

                }
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        areaDos.append("-> Materia: " + matery + "\n"
                + "- Nombre: " + name + "\n"
                + "- Nota mas alta: " + nota + "\n");
    }//GEN-LAST:event_mejorNotaActionPerformed

